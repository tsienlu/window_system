package com.vaolan.adserver.model;


public class ImpressLog {
	
	private String logId;
	private String adId;
	private String adMaterialId;
	private String impressUrl;
	private String consumNum;
	private String crowdId;
	private String uid;
	private String visitorIp;
	private String srcUrl;
	
	public String getLogId() {
		return logId;
	}
	public void setLogId(String logId) {
		this.logId = logId;
	}
	public String getAdId() {
		return adId;
	}
	public void setAdId(String adId) {
		this.adId = adId;
	}
	public String getAdMaterialId() {
		return adMaterialId;
	}
	public void setAdMaterialId(String adMaterialId) {
		this.adMaterialId = adMaterialId;
	}
	public String getImpressUrl() {
		return impressUrl;
	}
	public void setImpressUrl(String impressUrl) {
		this.impressUrl = impressUrl;
	}
	public String getConsumNum() {
		return consumNum;
	}
	public void setConsumNum(String consumNum) {
		this.consumNum = consumNum;
	}
	public String getCrowdId() {
		return crowdId;
	}
	public void setCrowdId(String crowdId) {
		this.crowdId = crowdId;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getVisitorIp() {
		return visitorIp;
	}
	public void setVisitorIp(String visitorIp) {
		this.visitorIp = visitorIp;
	}
	public String getSrcUrl() {
		return srcUrl;
	}
	public void setSrcUrl(String srcUrl) {
		this.srcUrl = srcUrl;
	}
	
}
