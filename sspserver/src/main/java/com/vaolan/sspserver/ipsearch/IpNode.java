package com.vaolan.sspserver.ipsearch;

public class IpNode {
	
	private long ip1;
	private long ip2;
	private  String addr;
	public long getIp1() {
		return ip1;
	}
	public void setIp1(long ip1) {
		this.ip1 = ip1;
	}
	public long getIp2() {
		return ip2;
	}
	public void setIp2(long ip2) {
		this.ip2 = ip2;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
}
