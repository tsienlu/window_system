package com.vaolan.sspserver.model;


/**
 * 伸缩的广告，变化的素材的尺寸
 * @author chenjinzhao
 *
 */
public  class VarSizeBean {
	
	
	private String sizeBigWidth;
	
	private String sizeBigHeight;
	
	private String sizeSmallWidth;
	
	private String sizeSmallHeight;

	public String getSizeBigWidth() {
		return sizeBigWidth;
	}

	public void setSizeBigWidth(String sizeBigWidth) {
		this.sizeBigWidth = sizeBigWidth;
	}

	public String getSizeBigHeight() {
		return sizeBigHeight;
	}

	public void setSizeBigHeight(String sizeBigHeight) {
		this.sizeBigHeight = sizeBigHeight;
	}

	public String getSizeSmallWidth() {
		return sizeSmallWidth;
	}

	public void setSizeSmallWidth(String sizeSmallWidth) {
		this.sizeSmallWidth = sizeSmallWidth;
	}

	public String getSizeSmallHeight() {
		return sizeSmallHeight;
	}

	public void setSizeSmallHeight(String sizeSmallHeight) {
		this.sizeSmallHeight = sizeSmallHeight;
	}
	
	

}
